<?php
// Define a variable $radio and set it to 25.
$radio = 25;

// Define a constant named PI and set it to the value 3.14.
define('PI', 3.14);

// Calculate and echo the area of a circle using the formula: PI * radius^2
echo PI * ($radio ** 2);

// Increase the value of $radio by 5.5.
$radio += 5.5;

// Echo a line break followed by the updated value of $radio.
echo "<br>" . $radio;

// Define a variable $num and set it to 34.
$num = 34;

// Increase the value of $num by 10.
$num += 10;

// Echo a line break followed by the updated value of $num.
echo "<br>" . $num;

// Echo a line break followed by the floor value of $radio.
echo "<br>" . floor($radio);
?>
